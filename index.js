function Scooter (year, color, model) {
  this.year = year;
  this.color = color;
  this.model = model;
}

function Driver (name, age, experience) {
  this.name = name;
  this.age = age;
  this.experience = experience;
}

function PickupLocation (hotel, address, city) {
  this.hotel = hotel;
  this.address = address;
  this.city = city;
}